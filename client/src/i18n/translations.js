export function translate(language, key, vars = {}) {
  const message = translations[language]?.[key] ?? translations.ko[key] ?? key

  return Object.entries(vars).reduce(
    (result, [name, value]) => result.replaceAll(`{{${name}}}`, String(value)),
    message,
  )
}
